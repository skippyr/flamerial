#!/usr/bin/env bash

# Applies the Flamerial theme in bspwm.

# This file must be executed in ~/.config/bspwm/bspwmrc:
#     ~/.config/bspwm/flamerial.sh &

bspc config active_border_color \#7d0800;
bspc config focused_border_color \#7d0800;
bspc config normal_border_color \#695f44;
bspc config presel_feedback_color \#c76800;
